# Base ID for all AC6 items — unique across AP worlds.
#
# ENCODING RULE: every weapon/part code is BASE_ID + <AC6 part ID>, using the
# exact value from the game's internal WEAPON_ID / PROTECTOR_ID / etc. tables.
# The DLL recovers the part ID with one subtraction:
#     ac6_part_id = ap_item_id - BASE_ID
# No separate lookup table needed.
#
# Weapons exist as two separate items — Right (10xxxxxx / 30xxxxxx) and
# Left (15xxxxxx / 35xxxxxx) — so each arm slot item is independent.
#
# COAM and Victory use sentinel offsets that can never collide with a real
# part ID so the DLL can detect and skip them.
